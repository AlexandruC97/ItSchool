package com.itschool.Session4.Homework.UnaryOperators;

public class Ex1 {
    public static void main(String[] args) {
        int sameVariable = 10;

        System.out.println("Original value of the variable is " + sameVariable);

        ++sameVariable;

        System.out.println("Value after incrementing is " + sameVariable);

        --sameVariable;

        System.out.println("Value after decrementing is " + sameVariable);
    }
}
